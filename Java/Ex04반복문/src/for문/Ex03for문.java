package for문;

import java.util.Scanner;

public class Ex03for문 {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		System.out.print("정수 입력 : ");
		int num = sc.nextInt();
		
		
		for (int i = 1; i <= num; i++) {
			if (num % i == 0) {
				System.out.print(i + " ");
			}
		}
		System.out.println(" ");
		
		int sum = 0;
		for (int i = 1; i <= num; i++) {
			if (num % i == 0) {
				sum += i;
			}
		}
		System.out.println(sum);
		
		// 클린코드
		// 코드 길이가 짧다.
		// 유지보수가 용이하다.
		// 가독성 뛰어남.
		// 시간복잡도가 작다.
		
		
		

	}

}
