
public class Ex01변수 {

	public static void main(String[] args) {
		
		// 변수
		// 프로그래밍에서 데이터를 담을 수 있는 공간
		
		// a 라는 공간에 정수형(숫자) 데이터 3을 저장
		int a = 3; // 선언 및 초기화
		System.out.println(a);
		
		int b; // 선언 : 데이터가 들어갈 공간 만들기
		b = 5; // 할당 : 변수에 처음으로 값 대입
		System.out.println(b);
		b = 7; // 재할당
		System.out.println(b);
		// 변수의 선언은 클래스에서 딱 한 번만 가능 !!
		
		
		
		String c = "배고파요";
		System.out.println(c);

	}

}
